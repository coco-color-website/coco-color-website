export const colorDiagnosisCaveats = `
诊断注意事项：
1. 浅春 vs 浅夏：注意错视现象，暗色布上身后亮色布再上身时脸一瞬间会变白，可能产生误判。诊布明度比皮肤明度更高时需要排除。
2. 亮春 vs 亮冬：高彩度诊布比皮肤突出很多则要排除；衣服彩度过高会让视线集中到衣服上而非脸。
3. 柔夏 vs 柔秋：若面部和诊布一同变灰则需要排除；灰调头发会显得脸发灰显脏。
4. 深秋 vs 深冬：使用低明度诊布时需注意错视现象，需等视觉适应一段时间再判断；注意是脸变白还是脸和脖子产生分离。
`;

export const colorMatchingMethods = `
配色法：
- Tone on tone 同色配色：同一个色彩的不同明度、彩度配色，适合对比度大的类型。
- Tone in tone 相似色配色：相似色的相似色调配色，暖色到中性色/冷色到中性色，临近色调，适合对比度小的类型。
- Separation 分离配色：给人分离感、节奏感；相似色和色调、对比度大时都用无彩色整理。
- Contrast 撞色配色：两种颜色对比度大的配色，适合对比度大或适合颜色多的类型。
- Camaieu 单色配色：同一种颜色的相似色调配色，适合 tone in tone 且没有 second color 的情况。
`;

